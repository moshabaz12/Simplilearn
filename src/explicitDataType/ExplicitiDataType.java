package explicitDataType;

public class ExplicitiDataType {

	public static void main(String[] args) {
		
		
		double a = 50.50;
		System.out.println("Double represents :"+a);
		float f=(float)a;
		System.out.println("Float represents :"+f);
		long b =(long)a;
		System.out.println("Long represents :"+b);
		int c =(int)b;
		System.out.println("Int represents :"+c);
	}

}